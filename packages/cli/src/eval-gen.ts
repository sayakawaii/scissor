import type { TraceEvent } from "./trace.js";

/**
 * trace -> eval flywheel (minimal): turn a real traced session into a *draft*
 * regression test. We pull the user's prompt and the files the agent produced
 * out of the trace and scaffold an EvalTask whose check asserts those artifacts
 * reappear. The draft is meant for human review — tighten the assertions (exact
 * contents, run the program, check stdout) and move it into the suite — but it
 * captures a real scenario automatically so the eval signal can grow from use.
 */

export interface EvalDraft {
  id: string;
  /** The user prompt the case replays. */
  prompt: string;
  /** Workspace-relative files the agent created/edited during the session. */
  files: string[];
  /** Suggested filename for the draft. */
  filename: string;
  /** Ready-to-review TypeScript source for the draft task. */
  code: string;
}

export interface GenerateOptions {
  /** Override the generated task id. */
  id?: string;
}

function firstPrompt(events: TraceEvent[]): string {
  const u = events.find((e) => e.type === "user" && typeof e.prompt === "string");
  return u ? String(u.prompt) : "";
}

function editedFiles(events: TraceEvent[]): string[] {
  const files: string[] = [];
  const seen = new Set<string>();
  for (const e of events) {
    if (e.type !== "tool") continue;
    if (e.ok === false) continue;
    if (e.name !== "write_file" && e.name !== "edit_file") continue;
    const p = typeof e.path === "string" ? e.path.replace(/\\/g, "/") : undefined;
    if (p && !seen.has(p)) {
      seen.add(p);
      files.push(p);
    }
  }
  return files;
}

function sessionId(events: TraceEvent[]): string {
  const s = events.find((e) => e.type === "session-start");
  return s && typeof s.sessionId === "string" ? s.sessionId : "session";
}

/** Build a draft EvalTask from a session's trace events. */
export function generateEvalDraft(events: TraceEvent[], opts: GenerateOptions = {}): EvalDraft {
  const prompt = firstPrompt(events);
  const files = editedFiles(events);
  const id = opts.id ?? `gen-${sessionId(events)}`;
  const filename = `${id}.eval.ts`;

  const filesLiteral = JSON.stringify(files);
  const code = `/**
 * DRAFT eval case generated by \`scissor eval-gen\` from a real session trace.
 *
 * It replays the original prompt and checks that the files the agent produced
 * reappear. This is a STARTING POINT — before adding it to the suite you should:
 *   - tighten the check: assert file *contents*, run the program, verify stdout;
 *   - add a setup() if the task needs pre-existing files;
 *   - give it a real title and tags,
 * then move the object into packages/cli/src/eval/bench-tasks.ts (or tasks.ts).
 *
 * Self-contained (only node:fs) so you can eyeball it as-is.
 */
import { promises as fs } from "node:fs";
import path from "node:path";

async function exists(dir: string, rel: string): Promise<boolean> {
  try {
    await fs.stat(path.join(dir, rel));
    return true;
  } catch {
    return false;
  }
}

export const GENERATED_TASKS = [
  {
    id: ${JSON.stringify(id)},
    title: "TODO: describe what this verifies",
    tags: ["generated"],
    prompt: ${JSON.stringify(prompt)},
    async check(dir: string) {
      const expected = ${filesLiteral} as string[];
      for (const rel of expected) {
        if (!(await exists(dir, rel))) {
          return { pass: false, detail: \`expected file missing: \${rel}\` };
        }
      }
      // TODO: tighten — assert file contents and/or run the program and check stdout.
      return { pass: true, detail: \`produced \${expected.length} expected file(s)\` };
    },
  },
];
`;

  return { id, prompt, files, filename, code };
}
